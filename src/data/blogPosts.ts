// Export all posts organized by language
export const blogPostsByLanguage: Record<Language, BlogPost[]> = {
  it: italianPosts,
  en: englishPosts,
  fr: frenchPosts,
  es: spanishPosts,
  de: germanPosts
};

export const getBlogPost = (slug: string, language: Language = 'it'): BlogPost | undefined => {
  return blogPostsByLanguage[language].find(post => post.slug === slug);
};

export const getAllBlogPosts = (language: Language = 'it'): BlogPost[] => {
  return blogPostsByLanguage[language].sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
};

